package dgtic.modelo.entidades.autobus;

public class TipoUsoAutobus {
    private Integer idTipoUsoAutobus;
    private String tipoUsoAutobus;
    private String estatus;

    public Integer getIdTipoUsoAutobus() {
        return idTipoUsoAutobus;
    }

    public void setIdTipoUsoAutobus(Integer idTipoUsoAutobus) {
        this.idTipoUsoAutobus = idTipoUsoAutobus;
    }

    public String getTipoUsoAutobus() {
        return tipoUsoAutobus;
    }

    public void setTipoUsoAutobus(String tipoUsoAutobus) {
        this.tipoUsoAutobus = tipoUsoAutobus;
    }

    public String getEstatus() {
        return estatus;
    }

    public void setEstatus(String estatus) {
        this.estatus = estatus;
    }

}
