DROP TABLE IF EXISTS EMPLOYEE;
CREATE TABLE EMPLOYEE (
  ID INTEGER NOT NULL AUTO_INCREMENT,
  NAME VARCHAR(255),
  SALARY BIGINT,
  STREET VARCHAR(255),
  CITY VARCHAR(255),
  STATE VARCHAR(255),
  ZIP_CODE VARCHAR(255),
  PRIMARY KEY (ID)
);
DROP TABLE IF EXISTS COMPANY;
CREATE TABLE COMPANY (
  ID INTEGER NOT NULL,
  STREET VARCHAR(255),
  CITY VARCHAR(255),
  STATE VARCHAR(255),
  ZIP_CODE VARCHAR(255),
  PRIMARY KEY (ID)
);
